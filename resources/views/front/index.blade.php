                            <p>
                                Asian-Pacific Childrenʼs Convention in Fukuoka
                                (APCC)APCC:Asian-Pacific Children's Convention was created as
                                a commemorative event of the 1989 Asian-Pacific Exposition, in
                                celebration of Fukuoka Cityʼs 100th anniversary. In 1989, the
                                first APCC was implemented by Junior Chamber International
                                Fukuoka, inpartial commemoration of the national convention of
                                the Junior Chamber International Japan. The JCI Fukuoka
                                members who launched this project believed that by providing
                                children a chance to be exposed to diverse values in the world
                                at an early stage of their life – as 11-year-olds – while they
                                are still sensitive and unsophisticated, they could cultivate
                                a sensibility of peace and coexistence with people from
                                different backgrounds. Through the establishment of this
                                project, JCI Fukuoka members were also eager to contribute to
                                enhancing the attraction of Fukuoka as a cosmopolitan city,
                                which historically and geographically has had a key role in
                                exchange between Japan and the cultures of continental Asia.
